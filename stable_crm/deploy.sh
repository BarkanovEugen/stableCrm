echo ""
log "üîê VK ID –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏—è –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∞ –¥–ª—è –¥–æ–º–µ–Ω–∞: $VK_ID_REDIRECT_URI"
